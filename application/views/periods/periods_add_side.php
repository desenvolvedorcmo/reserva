<dl>
  <dt>Formato de hora</dt>
  <dd>Os formatos suportados para os campos Início e Término são <span>HHMM</span> or <span>HH:MM</span>. Por exemplo, 09h30 ou 14h20. Você também deve incluir o 0 à esquerda se estiver usando o formato HHMM.</dd>
	<dd>Todos os horários também devem ser inseridos no formato de <span>24 horas</span> e estar entre 00:00 e 23:59.</dd>
</dl>
